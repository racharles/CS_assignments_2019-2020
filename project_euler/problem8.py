# starting from the beginning, iterate through all the possible combinations of
# adjacent digits
def adjacent_digits(ints,n):
    """Returns the largest product that can be created using n adjacent digits
    from string of ints"""
    products = []
    for i in range(len(ints)-n+1):
        # slice out nums into a list n long
        num_list = list(ints[i:i+n])

        for i in range(len(num_list)):
            # convert string to ints
            num_list[i] = int(num_list[i])
        # using numpy to multiply the numbers together, since i'm using 3.7 and
        # math.prod added in 3.8, and convert to 64 bit since the num can be too long
        products.append(numpy.prod(num_list, dtype=numpy.int64))
    return max(products)


